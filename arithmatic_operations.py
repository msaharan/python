## addition
a = 10
b = 20
c = a + b
print(c) 

## subtraction
c = a - b
print(c)

## multiplication
c = a * b
print(c)

## division
c = a / b
print(c)

## modulus
c = a % b
print(c)

## exponentiation
c = a ** b
print(c)

## floor division
c = a // b
print(c)

# Comparison operations
## equal
a = 10
b = 20
c = a == b
print(c)

## not equal
c = a != b
print(c)

## greater than
c = a > b
print(c)

## less than
c = a < b
print(c)

## greater than or equal to
c = a >= b
print(c)

## less than or equal to
c = a <= b
print(c)